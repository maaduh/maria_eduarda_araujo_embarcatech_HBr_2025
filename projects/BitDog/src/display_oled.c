int dma_channel;
dma_channel_config dma_config;

void render_on_display_dma(const uint8_t *data, size_t length) {
    printf("Iniciando transferência DMA...\n");

    // Configura o DMA para transferir os dados da imagem
    dma_channel_set_read_addr(dma_channel, data, false);  // Endereço de origem (dados da imagem)
    dma_channel_set_trans_count(dma_channel, length, true);  // Número de bytes a transferir

    // Aguarda a conclusão da transferência DMA
    dma_channel_wait_for_finish_blocking(dma_channel);

    printf("Transferência DMA concluída.\n");
}


void setup_dma() {
    // Reserva um canal DMA livre
    dma_channel = dma_claim_unused_channel(true);
    if (dma_channel == -1) {
        printf("Erro: Nenhum canal DMA disponível!\n");
        return;
    }
    printf("Canal DMA %d reservado.\n", dma_channel);

    // Configuração padrão do DMA
    dma_config = dma_channel_get_default_config(dma_channel);

    // Configura o DMA para transferir dados de 8 bits
    channel_config_set_transfer_data_size(&dma_config, DMA_SIZE_8);

    // Configura o DMA para usar o I2C1 como destino
    channel_config_set_dreq(&dma_config, DREQ_I2C1_TX);

    // Configura o canal DMA
    dma_channel_configure(
        dma_channel,          // Canal DMA
        &dma_config,         // Configuração do DMA
        &i2c1_hw->data_cmd,  // Endereço de destino (registrador de dados do I2C)
        NULL,                // Endereço de origem (será definido durante a transferência)
        0,                   // Número de transferências (será definido durante a transferência)
        false                // Não iniciar a transferência ainda
    );

    printf("DMA configurado com sucesso.\n");
}

